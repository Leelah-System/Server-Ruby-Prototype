class UserRole < ActiveRecord::Base

  #===========================================================================#
  #===   ASSOCIATIONS
  #===========================================================================#
  has_and_belongs_to_many :users

  #===========================================================================#
  #===   VALIDATIONS
  #===========================================================================#
  validates_presence_of :label

  #===========================================================================#
  #===   METHODS
  #===========================================================================#
  def to_s
    self.name
  end

  def is_admin_role?
    self.label == "admin"
  end

end
